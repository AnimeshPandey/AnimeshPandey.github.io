#!/usr/bin/env node
/**
 * generate-casebook-stats.mjs — build-time bridge between the Casebook
 * build and the root portfolio (design-backlog idea #5: skill-tag
 * cross-links).
 *
 * The portfolio's #skills section (root index.html) and the Casebook
 * (cases/) are two separate Eleventy builds merged only at deploy time —
 * there's no shared template layer between them, so the portfolio can't
 * read Casebook data directly. This script reads the Casebook's own
 * category counts from cases/src/_data/hub-facets.json (the same file
 * cases/.eleventy.js uses, and the same one tests/static/
 * assert-category-chip-counts.mjs already verifies against) and writes a
 * small static JSON file the portfolio's client-side JS can fetch, so a
 * skill chip like "React" can link to "/cases/companies/?category=react"
 * annotated with a real, current article count instead of a guess.
 *
 * Deliberately NOT committed to git (see .gitignore) — regenerated by
 * this script on every local `npm run build:deploy` and in CI
 * (static-pages.yml), so it can never silently drift out of sync with
 * hub-facets.json the way a checked-in snapshot could.
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const ROOT = path.join(path.dirname(fileURLToPath(import.meta.url)), '..');
const HUB_FACETS_PATH = path.join(ROOT, 'cases/src/_data/hub-facets.json');
const OUT_PATH = path.join(ROOT, 'assets/casebook-stats.json');

function main() {
  const raw = fs.readFileSync(HUB_FACETS_PATH, 'utf8');
  const hubFacets = JSON.parse(raw);

  const categories = {};
  for (const cat of hubFacets.categories || []) {
    categories[cat.id] = {
      label: cat.label,
      count: cat.totalCount != null ? cat.totalCount : cat.count,
    };
  }

  const out = {
    generatedAt: new Date().toISOString(),
    source: 'cases/src/_data/hub-facets.json',
    categories,
  };

  fs.mkdirSync(path.dirname(OUT_PATH), { recursive: true });
  fs.writeFileSync(OUT_PATH, JSON.stringify(out, null, 2) + '\n');
  console.log(
    `✓ Wrote ${OUT_PATH} (${Object.keys(categories).length} categories)`
  );
}

main();
